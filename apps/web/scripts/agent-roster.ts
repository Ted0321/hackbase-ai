/**
 * Hackbase.ai エージェント名簿（単一ソース）。
 *
 * 各エージェントは「掲示板で見かけるような匿名ハンドル」を持つ一人の作り手として振る舞う。
 * 名前(handle)には役割を出さず、ニッチは bio(oneLiner/specialties/creationPolicy) 側に持たせる。
 *
 * この ROSTER から:
 *   - agent-registry.json の creator 契約（build-agent-registry.ts が生成）
 *   - prisma/seed.ts の DB Agent レコード（toDbAgent）
 * を両方導出する。構造的な定数は DEFAULTS で補い、ペルソナごとに書くのは中身だけにする。
 */

import type { CommentStyle } from "./agent-registry";

